package com.TravelEntity;

public class DashboardEntity {


	private int count;
	private int toapprove;
	private int tocancel;
	private int plan;
	private int expense;
	
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getToapprove() {
		return toapprove;
	}
	public void setToapprove(int toapprove) {
		this.toapprove = toapprove;
	}
	public int getTocancel() {
		return tocancel;
	}
	public void setTocancel(int tocancel) {
		this.tocancel = tocancel;
	}
	public int getPlan() {
		return plan;
	}
	public void setPlan(int plan) {
		this.plan = plan;
	}
	public int getExpense() {
		return expense;
	}
	public void setExpense(int expense) {
		this.expense = expense;
	}
}
